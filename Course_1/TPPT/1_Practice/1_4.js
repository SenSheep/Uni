// 1. Создание переменной численного типа
let myNumber = 2;

// 2. Вывод значения и типа переменной
console.log("Initial value:", myNumber); // 2
console.log("Type of initial value:", typeof myNumber); // "number"

// 3. Прибавление к переменной ее типа
myNumber += typeof myNumber;

// 4. Вывод значения и типа полученного результата
console.log("New value:", myNumber); // "2number"
console.log("Type of new value:", typeof myNumber); // "string"
