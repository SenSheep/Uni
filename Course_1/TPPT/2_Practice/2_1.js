// Цикл for, который выводит числа от 1 до 5
for (let i = 1; i <= 5; i++) {
    console.log(`for loop iteration: ${i}`);
}

// Проверка четности чисел от 1 до 5
for (let k = 1; k <= 5; k++) {
    if (k % 2 === 0) {
        console.log(`${k} is even`);
    } else {
        console.log(`${k} is odd`);
    }
}

// Цикл while, который выводит числа от 1 до 5
let j = 1;
while (j <= 5) {
    console.log(`while loop iteration: ${j}`);
    j++;
}

